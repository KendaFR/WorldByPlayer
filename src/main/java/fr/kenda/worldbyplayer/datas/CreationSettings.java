package fr.kenda.worldbyplayer.datas;

public class CreationSettings {

    private String name = null;
    private int seed = 0;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSeed() {
        return seed;
    }

    public void setSeed(int seed) {
        this.seed = seed;
    }

}
